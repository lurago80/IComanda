-- 8. Verificar produtos ativos sem grupo
SELECT COUNT(*) AS ATIVOS_SEM_GRUPO
FROM PRODUTOESERVICO
WHERE (ATIVO = 1 OR ATIVO IS NULL)
AND (GRUPO IS NULL OR GRUPO = 0);

